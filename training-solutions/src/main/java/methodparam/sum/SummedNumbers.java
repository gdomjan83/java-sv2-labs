package methodparam.sum;

public class SummedNumbers {
    double positiveNumberSum;
    double negativeNumbersSum;

    public double getPositiveNumberSum() {
        return positiveNumberSum;
    }

    public double getNegativeNumbersSum() {
        return negativeNumbersSum;
    }

    public void setPositiveNumberSum(double positiveNumberSum) {
        this.positiveNumberSum = positiveNumberSum;
    }

    public void setNegativeNumbersSum(double negativeNumbersSum) {
        this.negativeNumbersSum = negativeNumbersSum;
    }
}
