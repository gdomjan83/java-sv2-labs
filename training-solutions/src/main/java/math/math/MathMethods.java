package math.math;

public class MathMethods {
    public static void main(String[] args) {
        System.out.println(Math.max(152, 260));
        System.out.println(Math.min(32, -46));
        System.out.println(Math.PI);
        System.out.println(Math.round(Math.PI));
        System.out.println(Math.round(4.487));
        System.out.println(Math.abs(-85));
        System.out.println(Math.negateExact(1));
        System.out.println(Math.addExact(45, 32));
        System.out.println(Math.subtractExact(100, 20));
        System.out.println(Math.multiplyExact(5, 12));
        System.out.println(Math.pow(2, 4));
        System.out.println(Math.incrementExact(3));
        System.out.println(Math.decrementExact(0));
        System.out.println(Math.random());
    }
}
